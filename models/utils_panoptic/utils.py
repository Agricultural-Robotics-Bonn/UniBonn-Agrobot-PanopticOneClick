class ASPPConv(nn.Sequential):
    def __init__(self, in_channels, out_channels, dilation):
        modules = [
            nn.Conv2d(in_channels, out_channels, 3, padding=dilation, dilation=dilation, bias=False),
            nn.BatchNorm2d(out_channels),
            nn.ReLU()
        ]
        super(ASPPConv, self).__init__(*modules)


class ASPPPooling(nn.Sequential):
    def __init__(self, in_channels, out_channels):
        super(ASPPPooling, self).__init__(
            nn.AdaptiveAvgPool2d(1),
            nn.Conv2d(in_channels, out_channels, 1, bias=False),
            nn.BatchNorm2d(out_channels),
            nn.ReLU())

    def forward(self, x):
        size = x.shape[-2:]
        for mod in self:
            x = mod(x)
        return F.interpolate(x, size=size, mode='bilinear', align_corners=False)


class ASPP(nn.Module):
    def __init__(self, in_channels, atrous_rates, out_channels=256):
        super(ASPP, self).__init__()
        modules = []
        modules.append(nn.Sequential(
            nn.Conv2d(in_channels, out_channels, 1, bias=False),
            nn.BatchNorm2d(out_channels),
            nn.ReLU()))

        rates = tuple(atrous_rates)
        for rate in rates:
            modules.append(ASPPConv(in_channels, out_channels, rate))

        modules.append(ASPPPooling(in_channels, out_channels))

        self.convs = nn.ModuleList(modules)

        self.project = nn.Sequential(
            nn.Conv2d(5 * out_channels, out_channels, 1, bias=False),
            nn.BatchNorm2d(out_channels),
            nn.ReLU(),
            nn.Dropout(0.5))

    def forward(self, x):
        res = []
        for conv in self.convs:
            res.append(conv(x))
        res = torch.cat(res, dim=1)
        return self.project(res)


"""
Get the params for weight decay
"""
def get_params_to_decay(blacklist, whitelist, named_modules, named_parameters):
  """
  Get only those parameters that we should to perform weight decay upon.
  The potential parameters list are given in the whitelist and blacklist
  respectively. Inspiration for this came from:
  https://github.com/karpathy/minGPT/blob/3ed14b2cec0dfdad3f4b2831f2b4a86d11aef150/mingpt/model.py#L136
  """

  #
  no_decay = set()
  decay = set()

  # Go through and grab all the matching modules/parameters
  for mn, m in named_modules:
    for pn, p in m.named_parameters():
      fpn = '%s.%s' % (mn, pn) if mn else pn # full param name

      # No decay on the biases
      if pn.endswith('bias'):
        no_decay.add(fpn)
      # Weights of blacklist modules will NOT be weight decayed
      elif pn.endswith('weight') and isinstance(m, blacklist):
        no_decay.add(fpn)
      # Weights of whitelist modules will be weight decayed
      elif pn.endswith('weight') and isinstance(m, whitelist):
        decay.add(fpn)

  # Validate that we considered every parameter
  param_dict = {pn: p for pn, p in named_parameters}
  inter_params = decay & no_decay
  union_params = decay | no_decay

  # Debug code, just in case
  assert len(inter_params) == 0, "parameters %s made it into both decay/no_decay sets!" % (str(inter_params), )
  assert len(param_dict.keys() - union_params) == 0, "parameters %s were not separated into either decay/no_decay set!" \
                                                  % (str(param_dict.keys() - union_params), )
  return param_dict, decay, no_decay
